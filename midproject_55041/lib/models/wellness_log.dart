class WellnessLog {
  final int sleepHours;
  final int waterIntake;
  final int stressLevel;
  final String mood;
  final DateTime date;

  WellnessLog({
    required this.sleepHours,
    required this.waterIntake,
    required this.stressLevel,
    required this.mood,
    required this.date,
  });
}